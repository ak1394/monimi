-module(mm_monitor_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
    Queue = {mm_queue,
    		   {mm_queue, start_link, []},
			   permanent, 5000, worker, [mm_queue]},
    Ping = {mm_ping,
    		   {mm_ping, start_link, []},
			   permanent, 5000, worker, [mm_ping]},
    Dns = {mm_dns,
    		   {mm_dns, start_link, []},
			   permanent, 5000, worker, [mm_dns]},
    DnsSup = {mm_dns_sup,
    		   {supervisor, start_link, [{local, mm_dns_sup}, mm_dns_sup, []]},
			   permanent, infinity, supervisor, [mm_dns]},
    
    Processes = [Queue, Ping, Dns, DnsSup],
    {ok, {{one_for_all, 1, 60}, Processes}}.

