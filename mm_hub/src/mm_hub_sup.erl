-module(mm_hub_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
    application:set_env(mm_hub, supervisor_started, erlang:now()),
    Database = {mh_database,
    		   {mh_database, start_link, []},
			   permanent, 5000, worker, [mh_database]},
    
    Status = {mm_status,
    		   {mm_status, start_link, []},
			   permanent, 5000, worker, [mm_status]},

    Hub = {mm_hub,
    		   {mm_hub, start_link, []},
			   permanent, 5000, worker, [mm_hub]},

	Processes = [Database, Status, Hub],

    % Max 1 restart per minute
    {ok, {{one_for_all, 1, 60}, Processes}}.

%% ====================================================================
%% Internal functions
%% ====================================================================

